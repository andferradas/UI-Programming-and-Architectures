//Java strings are objects

let teststring = "Doh"

// 'length' is a property or attribute of the teststring object
console.log("The length of the string is", teststring.length);

//The function "toUpperCase is a method of the teststring object "Doh"

//Outputs "function"
console.log("\n")
console.log(teststring.toUpperCase);

//Outputs "DOH" the result of calling the function
console.log("\nThe output of calling the function toUpperCase is", teststring.toUpperCase());